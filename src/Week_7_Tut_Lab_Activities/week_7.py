# === STEP 12: Inference on new images inside class subfolders ===

import os
import numpy as np
import matplotlib.pyplot as plt
from tensorflow.keras.preprocessing import image

TEST_IMAGE_DIR = '/content/drive/MyDrive/Week_7_Tut_Lab_Activities/Assets/insects_dataset/test_data'

def load_and_preprocess_image(img_path, img_size=IMG_SIZE):
    img = image.load_img(img_path, target_size=img_size)
    img_array = image.img_to_array(img) / 255.0
    img_array = np.expand_dims(img_array, axis=0)
    return img_array

def predict_new_images(test_dir):
    img_files = []
    for root, dirs, files in os.walk(test_dir):
        for f in files:
            if f.lower().endswith(('.jpg', '.jpeg', '.png')):
                img_files.append(os.path.join(root, f))

    if not img_files:
        print(f"No images found in '{test_dir}'.")
        return

    for path in img_files:
        img_arr = load_and_preprocess_image(path)

        preds = model.predict(img_arr, verbose=0)
        pred_idx = np.argmax(preds[0])
        confidence = preds[0][pred_idx]

        file_name = os.path.basename(path)
        true_folder = os.path.basename(os.path.dirname(path))

        print(f"Image: {file_name}")
        print(f"Actual folder: {true_folder}")
        print(f"Predicted: {class_names[pred_idx]} ({confidence*100:.2f}%)")

        img = image.load_img(path)
        plt.imshow(img)
        plt.title(f"Actual: {true_folder}\nPredicted: {class_names[pred_idx]} ({confidence*100:.2f}%)")
        plt.axis("off")
        plt.show()

print("Running inference...")
predict_new_images(TEST_IMAGE_DIR)